public class FormatSpecifier {
    public static void main(String[] args) {
        int age = 20;
        double marks = 89.456;
        String name = "Vathsalya";

        System.out.printf("Name: %s\n", name);
        System.out.printf("Age: %d\n", age);
        System.out.printf("Marks: %.2f\n", marks);
    }
}

